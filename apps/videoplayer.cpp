// ------------------- UI Helpers -------------------

void draw_text(OS::IPCClient &ipc, int x, int y, const char *str,
               uint32_t color, int scale) {
  while (*str) {
    if ((uint8_t)*str < 128) {
      const uint8_t *glyph = font8x8_basic[(uint8_t)*str];
      for (int r = 0; r < 8; r++) {
        for (int c = 0; c < 8; c++) {
          if (glyph[r] & (1 << c)) {
            for (int sy = 0; sy < scale; sy++)
              for (int sx = 0; sx < scale; sx++)
                ipc.put_pixel(x + c * scale + sx, y + r * scale + sy, color);
          }
        }
      }
    }
    x += 8 * scale;
    str++;
  }
}

void blend_rect(OS::IPCClient &ipc, int x, int y, int w, int h, uint32_t color,
                uint8_t alpha) {
  uint32_t *buf = ipc.get_buffer();
  if (!buf)
    return;
  int sw = ipc.get_width();
  int sh = ipc.get_height();

  uint32_t r1 = (color >> 16) & 0xFF;
  uint32_t g1 = (color >> 8) & 0xFF;
  uint32_t b1 = color & 0xFF;

  for (int j = 0; j < h; j++) {
    for (int i = 0; i < w; i++) {
      int px = x + i;
      int py = y + j;
      if (px >= 0 && px < sw && py >= 0 && py < sh) {
        uint32_t bg = buf[py * sw + px];
        uint32_t r2 = (bg >> 16) & 0xFF;
        uint32_t g2 = (bg >> 8) & 0xFF;
        uint32_t b2 = bg & 0xFF;

        uint32_t r = (r1 * alpha + r2 * (255 - alpha)) / 255;
        uint32_t g = (g1 * alpha + g2 * (255 - alpha)) / 255;
        uint32_t b = (b1 * alpha + b2 * (255 - alpha)) / 255;

        buf[py * sw + px] = (0xFF << 24) | (r << 16) | (g << 8) | b;
      }
    }
  }
}

// Decoder callbacks
static void video_alloc(void **samples, unsigned samples_size, void **mbs,
                        unsigned mbs_size, int errno_on_fail, void *alloc_arg) {
  *samples = malloc(samples_size);
  *mbs = malloc(mbs_size);
}

static void video_free(void *samples, void *mbs, void *alloc_arg) {
  free(samples);
  free(mbs);
}

static void video_log(const char *str, void *log_arg) {
  OS::Syscall::print(str);
}

extern "C" void _start() {
  OS::IPCClient ipc;
  if (!ipc.connect())
    OS::Syscall::exit(1);
  if (!ipc.create_window("Retro Video Player", 800, 600))
    OS::Syscall::exit(1);

  // Initialize Edge264
  Edge264Decoder *dec =
      edge264_alloc(0, video_log, NULL, 0, video_alloc, video_free, NULL);
  if (!dec) {
    OS::Syscall::print("Failed to alloc decoder\n");
    OS::Syscall::exit(1);
  }

  // Flagship Startup Sound
  OS::Syscall::print("FLAGSHIP: Calibrating Audio...\n");
  uint8_t boot_wave[1000];
  for(int i=0; i<1000; i++) boot_wave[i] = (i * 10) % 255;
  speaker_pcm(boot_wave, 1000);

  // Main Loop
  while (true) {
    // Vanta Black Background
    ipc.fill_rect(0, 0, 800, 600, 0xFF080808);

    // Premium Header
    blend_rect(ipc, 0, 0, 800, 80, 0xFF121212, 255);
    draw_text(ipc, 30, 30, "RETRO", 0xFFE50914, 2);
    draw_text(ipc, 120, 30, "FLAGSHIP PLAYER", 0xFFFFFFFF, 2);

    // Video Status
    draw_text(ipc, 30, 100, "Media: VIDEO.MP4", 0xFFAAAAAA, 1);
    draw_text(ipc, 30, 120, "Driver: Flagship Grade Audio (AC97)", 0xFF00FF00, 1);

    // Render a mock video frame / spectrum
    static int phase = 0;
    for (int i = 0; i < 40; i++) {
        int h = 50 + (i % 5) * 20 + (phase % 10);
        ipc.fill_rect(50 + i * 18, 500 - h, 10, h, 0xFFE50914);
    }
    phase++;

    ipc.flush();

    OS::gfx_msg_t msg;
    if (ipc.recv_msg(&msg)) {
      if (msg.type == OS::MSG_GFX_KEY_EVENT && msg.data.key.key == 27) {
        edge264_free(&dec);
        OS::Syscall::exit(0);
      }
    }
    OS::Syscall::sleep(5);
  }
}
