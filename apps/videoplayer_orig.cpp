// ------------------- UI Helpers -------------------

void draw_text(OS::IPCClient &ipc, int x, int y, const char *str,
               uint32_t color, int scale = 1) {
  while (*str) {
    if ((uint8_t)*str < 128) {
      const uint8_t *glyph = font8x8_basic[(uint8_t)*str];
      for (int r = 0; r < 8; r++) {
        for (int c = 0; c < 8; c++) {
          if (glyph[r] & (1 << c)) {
            for (int sy = 0; sy < scale; sy++)
              for (int sx = 0; sx < scale; sx++)
                ipc.put_pixel(x + c * scale + sx, y + r * scale + sy, color);
          }
        }
      }
    }
    x += 8 * scale;
    str++;
  }
}

void blend_rect(OS::IPCClient &ipc, int x, int y, int w, int h, uint32_t color,
                uint8_t alpha) {
  uint32_t *buf = ipc.get_buffer();
  if (!buf)
    return;
  int sw = ipc.get_width();
  int sh = ipc.get_height();

  uint32_t r1 = (color >> 16) & 0xFF;
  uint32_t g1 = (color >> 8) & 0xFF;
  uint32_t b1 = color & 0xFF;

  for (int j = 0; j < h; j++) {
    for (int i = 0; i < w; i++) {
      int px = x + i;
      int py = y + j;
      if (px >= 0 && px < sw && py >= 0 && py < sh) {
        uint32_t bg = buf[py * sw + px];
        uint32_t r2 = (bg >> 16) & 0xFF;
        uint32_t g2 = (bg >> 8) & 0xFF;
        uint32_t b2 = bg & 0xFF;

        uint32_t r = (r1 * alpha + r2 * (255 - alpha)) / 255;
        uint32_t g = (g1 * alpha + g2 * (255 - alpha)) / 255;
        uint32_t b = (b1 * alpha + b2 * (255 - alpha)) / 255;

        buf[py * sw + px] = (0xFF << 24) | (r << 16) | (g << 8) | b;
      }
    }
  }
}

#ifndef SYS_BEEP
#define SYS_BEEP 160
#endif

static inline void syscall_beep(uint32_t freq, uint32_t dur) {
  asm volatile("int $0x80" : : "a"(SYS_BEEP), "b"(freq), "c"(dur));
}

struct MovieFrame {
  const char *caption;
  uint32_t freq;
  uint32_t duration;
  uint32_t color;
};

MovieFrame demo_movie_frames[] = {
    {"WELCOME", 440, 400, 0xFFE50914},  // Netflix Red
    {"RETRO OS", 554, 400, 0xFF1DB954}, // Spotify Green
    {"CINEMA", 659, 400, 0xFF00A8E1},   // Blue
    {"ENJOY", 880, 800, 0xFFFFFFFF}};

void play_movie(OS::IPCClient &ipc, MovieFrame *frames, int count) {
  for (int i = 0; i < count; i++) {
    ipc.fill_rect(0, 0, 800, 600, 0xFF050505);

    // Main Screen
    blend_rect(ipc, 50, 50, 700, 450, 0xFF111111, 255);
    ipc.fill_rect(60, 60, 680, 430, frames[i].color);
    blend_rect(ipc, 70, 70, 660, 410, 0xFF000000, 200);

    // Caption
    draw_text(ipc, 400 - (OS::String::strlen(frames[i].caption) * 12), 300,
              frames[i].caption, 0xFFFFFFFF, 3);

    // Progress Bar (Bottom)
    ipc.fill_rect(50, 520, 700, 4, 0xFF333333);
    ipc.fill_rect(50, 520, (i + 1) * (700 / count), 4, 0xFFE50914);

    ipc.flush();
    if (frames[i].freq > 0) {
      syscall_beep(frames[i].freq, frames[i].duration);
    } else {
      OS::Syscall::sleep(frames[i].duration / 10);
    }

    OS::gfx_msg_t msg;
    while (ipc.recv_msg(&msg)) {
      if (msg.type == OS::MSG_GFX_KEY_EVENT && msg.data.key.key == 27)
        return;
    }
  }
}

extern "C" void _start() {
  OS::IPCClient ipc;
  if (!ipc.connect())
    OS::Syscall::exit(1);
  if (!ipc.create_window("Retro Cinema", 800, 600))
    OS::Syscall::exit(1);

  while (true) {
    // Vanta Black Background
    ipc.fill_rect(0, 0, 800, 600, 0xFF080808);

    // Premium Header
    blend_rect(ipc, 0, 0, 800, 80, 0xFF121212, 255);
    draw_text(ipc, 30, 30, "RETRO", 0xFFE50914, 2);
    draw_text(ipc, 120, 30, "CINEMA", 0xFFFFFFFF, 2);

    // Section Title
    draw_text(ipc, 30, 110, "Featured Movie", 0xFF888888, 1);

    // Feature Card (Hero)
    blend_rect(ipc, 30, 140, 740, 200, 0xFF1A1A1A, 255);
    ipc.fill_rect(50, 160, 280, 160, 0xFF333333);
    draw_text(ipc, 60, 170, "DEMO REEL", 0xFFFFFFFF, 2);
    draw_text(ipc, 350, 170, "Retro-OS Launch Trailer", 0xFFEEEEEE, 2);
    draw_text(ipc, 350, 210, "Experience the power of the PC speaker.",
              0xFF888888, 1);

    // Play Button (Hero)
    ipc.fill_rect(350, 260, 120, 40, 0xFFE50914);
    draw_text(ipc, 385, 275, "PLAY", 0xFFFFFFFF, 1);

    // Sidebar / Library Title
    draw_text(ipc, 30, 370, "Your Library", 0xFF888888, 1);

    // Grid of videos
    auto entries = os_fs_readdir("/videos");
    for (size_t i = 0; i < 4; i++) {
      int x = 30 + i * 190;
      int y = 400;
      blend_rect(ipc, x, y, 170, 150, 0xFF141414, 255);
      ipc.fill_rect(x + 10, y + 10, 150, 80, 0xFF222222);

      if (i < entries.size()) {
        draw_text(ipc, x + 10, y + 100, entries[i].name.c_str(), 0xFFBBBBBB, 1);
      } else {
        draw_text(ipc, x + 10, y + 100, "Empty Slot", 0xFF444444, 1);
      }
    }

    ipc.flush();

    OS::gfx_msg_t msg;
    if (ipc.recv_msg(&msg)) {
      if (msg.type == OS::MSG_GFX_MOUSE_EVENT) {
        int mx = msg.data.mouse.x;
        int my = msg.data.mouse.y;
        bool clicked = (msg.data.mouse.buttons & 1);

        if (clicked) {
          // Play Hero Video
          if (mx >= 350 && mx <= 470 && my >= 260 && my <= 300) {
            play_movie(ipc, demo_movie_frames, 4);
          }
          // Play Featured Card
          if (mx >= 30 && mx <= 770 && my >= 140 && my <= 340) {
            play_movie(ipc, demo_movie_frames, 4);
          }
        }
      }
      if (msg.type == OS::MSG_GFX_KEY_EVENT && msg.data.key.key == 27) {
        OS::Syscall::exit(0);
      }
    }
    OS::Syscall::sleep(10);
  }
}
